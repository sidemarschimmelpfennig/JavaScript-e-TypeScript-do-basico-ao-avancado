/*
OPERADORES LÓGICOS
 * && -> AND -> E

 * || -> OR  -> OU

 * !  -> NOT -> NÃO 

*/

//Ambas as expressoes devem ser verdadeiras pra retornar verdadeiro 
console.log(5 > 6 && 6 < 7)
//Uma ou Outra expressão devem ser verdadeiras  pra retornar verdadeiro 
console.log(5 > 6 && 6 < 7)
// Não deve ser igual a verdadeiro para retornar true pra retornar verdadeiro  e se eu colocar dois not retorna ao valor original !!
console.log(!(5 > 6 && 6 < 7))